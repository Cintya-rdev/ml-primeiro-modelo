# ========================
# VARIÁVEIS E TIPOS DE DADOS
# ========================

# Tipo texto (string)
nome = "Cintya"
curso = "Engenharia Química"

# Tipo número inteiro (int)
idade = 21
ano = 2025

# Tipo número decimal (float)
altura = 1.65
peso = 58.5

# Tipo verdadeiro/falso (boolean)
estudando = True
dormindo = False

# Exibindo os valores
print("Nome:", nome)
print("Curso:", curso)
print("Idade:", idade)
print("Altura:", altura)
print("Está estudando?", estudando)

# Verificando os tipos
print("\n--- Tipos de dados ---")
print(type(nome))
print(type(idade))
print(type(altura))
print(type(estudando))